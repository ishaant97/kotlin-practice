package `Day 4`

fun main() {
    var a = 30
    var b = 10
    //Relational Operators
    val greaterThen = a > b
    val lessThen = a < b
    val greaterThenOrEqualTo = a >= b
    val lessThenOrEqualTo = a <= b
    val notEqualTo = a != b
    val isEqualTo = a == b
    println("greaterThen of a and b is : $greaterThen")
    println("lessThen of a and b is : $lessThen")
    println("greaterThenOrEqualTo of a and b is : $greaterThenOrEqualTo")
    println("lessThenOrEqualTo of a and b is : $lessThenOrEqualTo")
    println("notEqualTo of a and b is : $notEqualTo")
    println("isEqualTo of a and b is : $isEqualTo")
}