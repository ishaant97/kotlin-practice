package `Day 4`

fun main() {
    var a = 30
    var b = 10
    //Assignment Operators
    a += b
    println("sum of a and b is : $a")
    a -= b
    println("subtract of a and b is : $a")
    a *= b
    println("multiplication of a and b is : $a")
    a /= b
    println("divide of a and b is : $a")
    a %= b
    println("modulus of a and b is : $a")
}